    《吳普》曰：黃孫一名王孫，一名蔓延，一名公草，一名海孫，神農雷公苦無毒。黃帝甘無毒，生西海山谷，及汝南城郭垣下，蔓延，赤文，莖葉相當。（《禦覽》）
