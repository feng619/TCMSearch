    案《說文》雲：桢，剛木也。東山經雲：太山上多桢木。郭璞雲：女桢也，葉冬不凋。《毛詩》雲：南山有杞。陸玑雲：木杞，其樹如樗（陳藏器作栗），一名狗骨，理白滑，其子爲木虻子，可合藥，《司馬相如賦》有女貞。師古曰：女貞樹，冬夏常青，未嘗凋落，若有節操，故以名爲焉。陳藏器雲：冬青也。
    
**木蘭
    味苦寒。
