    《吳普》曰：狗脊一名狗青，一名赤節，神農苦，桐君黃帝岐伯雷公扁鵲甘無毒李氏小溫，如萆薢，莖節如竹，有刺，葉圓赤，根黃白，亦如竹根，毛有刺。岐伯經雲：莖長節，葉端員青赤，皮白有赤脈。
    《名醫》曰：一名強膂，一名扶蓋，一名扶筋，生常山，二月八月，採根暴乾。
    案《廣雅》雲：潔，狗脊也。玉篇雲：狗脊根也。《名醫》别出契條，非。
    
**石龍芮
    味苦平。
    主風寒濕痹，心腹邪氣，利關節，止煩滿。久服，輕身明目，不老。一名魯果能（《禦覽》作食果），一名地椹，生川澤石邊。
    《吳普》曰：龍芮一名薑苔，一名天豆，神農苦平岐伯酸，扁鵲李氏大寒，雷公鹹無毒，五月五日採。（《禦覽》）
    《名醫》曰：一名石能，一名彭根，一名天豆，生太山，五月五日採子，二月八月採皮，陰乾。
    案《範子計然》雲：石龍芮，出三輔，色黃者善。
    
**茅根
    味甘寒。
    主勞傷虛羸，補中益氣，除淤血，血閉寒熱，利小便，其苗，主下水。一名蘭根，一名茹根。生山谷田野。
    《名醫》曰：一名地管，一名地筋，一名兼杜，生楚地，六月採根。
    案《說文》雲：茅，菅也。菅，茅也。《廣雅》雲：菅，茅也。《爾雅》雲：白華野菅。郭璞雲：菅，茅屬。詩雲：白華菅兮，白茅束兮。《傳》雲：白華，野菅也，已漚，爲菅。
    
**紫菀
    味苦溫。
    主咳逆上氣，胸中寒熱結氣，去蠱毒痿蹶，安五藏。生山谷。
    《吳普》曰：紫菀，一名青苑。（《禦覽》）
    《名醫》曰：一名紫茜，一名青苑，生房陵及真定邯鄲，二月三月，採根，陰乾。
    案《說文》雲：菀，茈菀，出漢中，房陵。陶宏景雲：白者名白菀。《唐本》注雲：白菀，即女菀也。
    
**紫草
    味苦寒。
    主心腹邪氣五疸，補中益氣，利九竅，通水道。一名紫丹，一名紫芙（《禦覽》引雲：一名地血，《大觀本》，無文）。生山谷。
    《吳普》曰：紫草節赤，二月花。（《禦覽》）
    《名醫》曰：生砀山及楚地，三月採根，陰乾。
    案《說文》雲：茈，草也，藐，茈草也，草也，可以染留黃。《廣雅》雲：茈，草也。《山海經》雲：勞山多茈草。郭璞雲：一名紫，中染紫也。《爾雅》雲：藐，茈草。郭璞雲：可以染紫。
    
**敗醬
    味苦平。
    主暴熱火創，赤氣，疥搔，疸痔，馬鞍，熱氣。一名鹿腸。生川谷。
    《名醫》曰：“一名鹿首，一名馬草，一名澤敗，生江夏，八月採根曝乾。
    案《範子計然》雲：敗醬出三輔。陶宏景雲：氣如敗醬。故以爲名。
    
**白鮮
    味苦寒。
    主頭風，黃疸，咳逆，淋瀝，女子陰中腫痛，濕痹死肌，不可屈伸，起止行步。生川谷。
    《名醫》曰：生上谷及冤句，四月五月，採根陰乾。
    案陶宏景雲：俗呼爲白羊鮮，氣息正似羊膻或名白膻。
    
**酸醬
    味酸平。
    主熱煩滿，定誌益氣，利水道，産難吞其實立産。一名醋醬。生川澤。
    《吳普》曰：酸醬，一名酢醬。（《禦覽》）
    《名醫》曰：生荊楚，及人家田園中，五月採，陰乾。
    案《爾雅》雲：葴，寒醬。郭璞雲：今酸醬草，江東呼曰苦葴。
    
**紫參
    味苦辛寒。
    主心腹積聚，寒熱邪氣。通九竅，利大小便。一名牡蒙。生山谷。
    《吳普》曰：伏蒙，一名紫參，一名泉戎，一名音腹，一名伏菟，一名重傷。神農黃帝苦，李氏小寒，生河西山谷或宛句商山，圓聚生，根黃赤有文，皮黑中紫，五月花紫，赤實黑，大如豆，三月採根。（《禦覽》《大觀本》節文）
    《名醫》曰：一名眾戎，一名童腸：一名馬行，生河西及冤句，三月採根，火炙使紫色。
    案《範子計然》雲：紫參出三輔，赤青色者善。
    
**藁本
    味辛溫。
    主婦人疝瘕，陰中寒腫痛，腹中急，除風頭痛，長肌膚：說顔色。一名鬼卿，一名地新。生山谷。
    《名醫》曰：一名微莖，生崇山，正月二月採根暴乾，三十日成。
    案《廣雅》雲：山芷蔚香，藁本也。《管子·地員篇》雲：五臭疇生藁本。《荀子·大略篇》雲：蘭芷藁本，浙于蜜醴，一佩易之。樊光注《爾雅》雲：藁本一名麋蕪，根名靳芷，歸作藁，非。
    
**石韋
    味苦平。
    主勞熱邪氣，五癃閉不通，利小便水道。一名石。生山谷石上。
    《名醫》曰：一名石皮，生華陰山谷，不聞水及人聲者，良，二月採葉，陰乾。
    
**萆薢
    味苦平。
    主腰背痛，強骨節，風寒濕，周痹，惡創不瘳，熱氣。生山谷。
    《名醫》曰：一名赤節，生真定，八月採根曝乾。
    案《博物誌》雲：苃葜與萆薢相亂。
    
**白薇
    味苦平。
    主暴中風，身熱肢滿，忽忽不知人，狂惑，邪氣，寒熱酸痋，溫瘧、洗洗發作有時。生川谷。
    《名醫》曰：一名白幕，一名薇草，一名春草，一名骨美，生平原，三月三日，採根陰乾。
    
**水萍
    味辛寒。
    主暴熱身癢（藝文類聚初學記癢，此是），下水氣勝酒，長須發（藝文類聚作烏發），消渴。久服輕身。一名水華（藝文類聚引雲：一名水廉）。生池澤。
    《吳普》曰：水萍一名水廉，生澤水上，葉員小，一莖，一葉，根入水，五月華白，三月採，日乾。（《禦覽》）
    《名醫》曰：一始水白，一名水蘇，生雷澤，三月採，曝乾。
    案《說文》雲：萍，蓱也，無根，浮水而生者。萍，蘋也，（上卄下賓），大萍也。《廣雅》雲：薸，萍也。《夏小正》雲：七月湟潦生蘋。《爾雅》雲：萍蘋。郭璞雲：水中浮萍，江東謂之薸.又其大者蘋。《毛詩》雲：于以採蘋。《傳》雲：蘋，大萍也。
    《範子計然》曰：水萍出三輔，色青者善。《《淮南子》·原道訓》雲：萍樹根于水。高誘雲：萍，大蘋也。
    
**王瓜
    味苦寒。
    主消渴内痹淤血，月閉，寒熱，酸疼，益氣，俞聾。一名土瓜。生平澤。
    《名醫》曰：生魯地田野，及人家垣牆間，三月採根，陰乾。
    案《說文》雲：萯，王萯也。《廣雅》雲：葵菇，瓜（婁瓜），王瓜也。《夏小正》雲：四月王萯秀。《爾雅》雲：鉤葵菇。郭璞雲：鉤，（婁瓜）也。一名王瓜，實如瓝瓜，正赤，味苦，月令，王瓜生。鄭元雲：月令雲，王萯生，孔穎連雲：疑王萯，則王瓜也。《管子·地員篇》，剽土之次，曰：五沙：其種大萯細萯，白莖青秀以蔓。本草圖經雲：大，即王萯也。亦謂之土瓜，自别是一物。
    
**地榆
    味苦微寒。
    主婦人乳痓痛，七傷帶下病，止痛。除惡肉，止汗，療金創（《禦覽》引雲：主消酒，又雲明目，《大觀本草》，消酒作黑字，而無明目）。生山谷。
    《名醫》曰：生桐柏及冤句，二月八月，採根，暴乾。
    案《廣雅》雲：菗蒢，地榆也。陶宏景雲：葉似榆而長，初生布地，而花子紫黑色如豉，故中玉豉。
    
**海藻
    味苦寒。
    主瘿瘤氣，頸下核，破散結氣，癰腫症瘕堅氣，腹中上下鳴，下水十二腫。一名落首。生池澤。
    《名醫》曰：一名藫，生東海，七月七日採，暴乾。
    案《說文》雲：薻，水草也，或作藻。《廣雅》雲：海蘿，海藻也。《爾雅》雲：薅，海藻也。郭璞雲：藥草也。一名海蘿，如亂發，生海中。本草雲：又薻石衣。
    郭璞雲：水苔也，一名石發，江東食之，或曰藫，葉似而大，生水底也，亦可食。
    
**澤蘭
    味苦微溫。
    主乳婦内（《禦覽》作衄血），中風餘疾，大腹水腫，身面四肢浮腫，骨節中水，金創癰腫創膿。一名虎蘭，一名龍棗。生大澤傍。
    《吳普》曰：澤蘭，一名水香，神農黃帝岐伯桐君酸無毒，李氏溫，生下地水傍，葉如蘭，二月生，香，赤節，四葉相值枝節間。
    《名醫》曰：一名虎蒲，生汝南，三月三日採，陰乾。
    案《廣雅》雲：虎蘭，澤蘭也。
    
**防己
    味辛平。
    主風寒溫瘧熱氣諸癰，除邪，利大小便。一名解離（《禦覽》作石解引雲：通湊理，利九竅，《大觀本》，六字黑）。生川谷。
    《吳普》曰：木防己，一名解離，一名解燕，神農辛，黃帝岐伯桐君苦無毒，李氏大寒，如艿，莖蔓延，如艽，白根外黃似桔梗，内黑又如車輻解，二月八月十月，採根。（《禦覽》）
    《名醫》曰，生漢中，二月八月，採根陰乾。
    案《範子計然》雲：防已出漢中旬陽。
    
**款冬花
    味辛溫。
    主咳逆上氣，善喘，喉痹，諸驚痫，寒熱邪氣。一名橐吾（《禦覽》作石），一名顆東（《禦覽》作顆冬），一名虎須，一名免奚。生山谷。
    《吳普》曰：款冬十二月，花黃白。（藝文類聚）
    《名醫》曰：一名氏冬，生常山及上黨水傍，十一月，採花陰乾。
    案《廣雅》雲：苦萃款東也。《爾雅》雲：莬奚顆東。郭璞雲：款冬也。紫赤華生水中。西京雜記雲：款冬，華于嚴冬。傳鹹款冬賦序曰：仲冬之月，冰淩積雪，款冬獨敷華豔。
    
**牡丹
    味苦辛寒。
    主寒熱，中風，瘈疭，痙，驚痫，邪氣，除症堅，淤血留捨腸胃，安五臟，療癰創。一名鹿韭，一名鼠姑。生山谷。
    《吳普》曰：牡丹，神農岐伯辛，李氏小寒，雷公桐君苦無毒，黃帝苦有毒，葉如蓬相植，根如柏，黑中有核，二月採，八月採，日乾，人食之，輕身益夀。（《禦覽》）
    《名醫》曰：生巴郡及漢中，二月八月，採根陰乾。
    案《廣雅》雲：白朮，牡丹也。《範子計然》雲：牡丹出漢中河内，赤色者亦善。
    
**馬先蒿
    味平。
    主寒熱，鬼注，中風濕痹，女子帶下病，無子。一名馬尿蒿。生川澤。
    《名醫》曰：生南陽。
    案《說文》雲：蔚，牡蒿也。《廣雅》雲：因塵，馬先也。《爾雅》雲：蔚，牡菣。
    郭璞雲：無子者。《毛詩》雲：匪莪伊蔚。《傳》雲：菣，牡菣也。陸玑雲：三月始生，七月華，華似胡麻華而紫赤，八月爲角，角似小豆，角銳而長，一名馬新蒿，案新先聲相近。
    
**積雪草
    味苦寒。
    主大熱，惡創癰疽，浸淫，赤熛，皮膚赤，身熱。生川谷。
    《名醫》曰：生荊州。
    案陶宏景雲：荊楚人以葉如錢，謂爲地錢草，徐儀藥圖名連錢草。本草圖經雲：鹹洛二京亦有，或名胡薄荷。
    
**女菀
    （《禦覽》作菀）
