右六味，末之，煉蜜和丸，梧子大，飲服十丸，日三，以知為度。

**16.
腎著之病，其人身體重，腰中冷，如坐水中，形如水狀，反不渴，小便自利，飲食如故，病屬下焦，身勞汗出，衣一作表裏冷濕，久久得之，腰以下冷痛，腹重如帶五千錢，甘薑苓朮湯主之。

