    《名醫》曰：一名羊泉，一名饴，生蜀郡。
    案《廣雅》雲：桼姑，艾但鹿何，澤也。《唐本》注雲：此草，一名漆姑。
    
**爵床
    味鹹寒。
    主腰脊痛，不得着床，俯仰艱難，除熱，可作浴湯。生川谷及田野。
    《吳普》曰：爵床，一名爵卿。（《禦覽》）
    《名醫》曰：生漢中。
    案别本注雲：今人名爲香蘇。
    
**假蘇
    味辛溫。
    主寒熱，鼠瘻，瘰疬生創，破結聚氣，下淤血，除濕痹，一名鼠蓂，生川澤。（舊在菜部，今移）
    《吳普》曰：假蘇一名鼠實，一名薑芥也。（《禦覽》）名荊芥，葉似落藜而細，蜀中生啖之。（蜀本注）
    《名醫》曰：一名薑芥，生漢中。
    案陶宏景雲：即荊芥也，薑荊聲訛耳，先居草部中，令人食之，錄在菜部中也。
    
**翹根
    味甘寒平（《禦覽》作味苦平）。
    主下熱氣，益陰精，令人面說好，明目。久服輕身耐老。生平澤。（舊在《唐本》退中，今移）
    《吳普》曰：翹根，神農雷公甘有毒，三月八月採，以作蒸，飲酒病人。（《禦覽》）
