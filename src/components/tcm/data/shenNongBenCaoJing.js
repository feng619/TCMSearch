    案《說文》雲：雞知時畜也。籀文作雞，肪，肥也。腸，大小腸也。膍鳥胵，胵鳥胃也。工（上艹下囗内必），糞也。翮羽，莖也。羽，鳥長毛也，此作肶，省文，尿，即（上艹下囗内必）字，古文，從。亦（上艹下囗内必）假音字也。
    
**鴈肪
    味甘平。主風攣，拘急，偏枯，氣不通利，久服，益氣不饑，輕身耐老。一名鹜肪。生池澤。
    《吳普》曰：鴈肪神農岐伯雷公甘無毒（《禦覽》有鹜肪二字，當作一名鹜肪），殺諸石藥毒。（《禦覽》引雲：採無時）
    《名醫》曰：生江南，取無時。
    案《說文》雲：鴈，鵝也。鹜，舒凫，也。《廣雅》雲：鵝，倉鴈也。凫，鹜鴨也。《爾雅》雲：舒鴈，鵝。郭璞雲：禮《記》曰，出如舒鴈。今江東呼，又舒凫，鹜。郭璞
    雲：鴨也。方言雲：鴈，自關而東，謂之鵝。南楚之外，謂之鵝，或謂之倉。據《說文》雲：别有鴈，以爲鴻鴈字無鴨字，鴨，即鴈之急音，此鴈肪，即鵝鴨脂也。當作鴈字，《名醫》不曉，别出鹜肪條，又出白鴨鵝條，反疑此爲鴻鴈，何其謬也。陶蘇皆亂說之。
    上禽，上品二種，舊同。
    
