    案《說文》雲：荔，草也，似蒲而小，根可作刷。《廣雅》雲：馬薤，荔也。月令雲：仲冬之月，荔挺出。鄭雲：荔挺，馬薤也。高誘注《淮南子》雲：荔馬，荔草也。通俗文雲：一名馬蘭。顔之推雲：此物河北平澤率生之，江東頗多種于階庭，但呼爲早蒲，故不識馬薤。
    
