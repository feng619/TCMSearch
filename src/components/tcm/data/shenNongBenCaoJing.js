    味篇雲：和之美者，陽朴之薑，高誘注，陽朴地名在蜀郡，司馬相如上林賦，有茈薑雲雲。
    
**枲耳實
    味甘溫。
    主風頭，寒痛，風濕，周痹，四肢拘攣，痛，惡肉死肌。久服益氣，耳目聰明，強誌輕身。一名胡枲，一名地葵。生川谷。
    《名醫》曰：一名葹，一名常思，生安陸及六安田野，實熟時採。
    案《說文》雲：荪，卷耳也。苓，卷耳也。《廣雅》雲：苓，耳葹，常枲，胡枲，枲耳也。《爾雅》雲：蒼耳，苓耳。郭璞雲：江東呼爲常枲，形似鼠耳，叢生如盤。《毛詩》雲：採採卷耳。《傳》雲：卷耳，苓耳也。陸玑雲：葉青，白色，似胡荽，白華，細莖蔓生，可煮爲茹，滑而少味，四月中生子，正如婦人耳珰，今或謂之耳珰草，鄭康成謂是白胡荽，幽州人謂之爵耳。《淮南子》覽冥訓雲：位賤尚枲。高誘雲：枲者，枲耳，菜名也。幽冀謂之檀菜，雒下謂之胡枲。
    
