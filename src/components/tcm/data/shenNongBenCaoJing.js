    案《廣雅》雲：木欄桂欄也。劉逵注蜀都賦雲：木蘭，大樹也，葉似長生，冬夏榮，常以冬華，其實如小柿，甘美。南人以爲梅，其皮可食。顔師古注漢書雲：
    皮似椒而香，可作面膏藥。
    
**蕤核
