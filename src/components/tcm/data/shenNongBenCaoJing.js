    《名醫》曰：生九江，採無時。
    案《說文》雲：鯉，鳣也。鳣，鯉也。《爾雅》雲：鯉鳣.捨人雲：鯉，一名鳣.郭璞注鯉雲：今赤鯉魚。注鳣雲：大魚似鲟。《毛詩》雲：鳣鲔發發。《傳》雲：鳣，鯉也，據此知郭璞别爲二，非矣。古今注雲：兖州人呼赤鯉爲赤驥，謂青鯉爲青馬，黑鯉爲元駒，白鯉爲白骐，黃鯉爲黃雉。
