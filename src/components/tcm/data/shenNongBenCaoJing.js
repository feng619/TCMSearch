    主風，洗洗，霍亂，洩利，腸鳴上下無常處，驚痫，寒熱百疾。生川谷，或山陽。
    《吳普》曰：女菀，一名白菀，一名識女苑。（《禦覽》）
    《名醫》曰：一名白菀，一名織女菀，一名茆，生漢中，正月二月採，陰乾。
    案《廣雅》雲：女腸，女菀也。
    
**王孫
    味苦平。
