    案《說文》雲：芷，（上艹下器）也。（上艹下器），楚謂之籬。晉謂之（上艹下器），齊謂之芷。《廣雅》雲：白芷，其葉謂之藥。《西山經》雲：號山，其草多藥（上艹下器）。郭璞雲：藥，白芷，别名（上艹下器），香草也。《淮南子》脩務訓雲：身苦秋藥被風。高誘雲：藥，白芷，香草也。王逸注《楚詞》雲：藥，白芷，按《名醫》一名莞雲雲，似即《爾雅》莞，苻離，其上鬲，而《說文》别有，夫離也。蒚，夫蓠上也，是非一草。捨人雲：白蒲一名苻離，楚謂之莞，豈蒲與芷相似，而《名醫》誤合爲一乎。或《說文》雲：楚謂之蓠，即夫籬也，未可得詳，舊作芷，非。
    
